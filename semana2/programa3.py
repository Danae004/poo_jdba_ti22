variable1 = 10 #declaracion de variables a usar
variable2 = 5
#Codigo para realizar la suma
print("El resultado de la suma es {}+{}={}".format(variable1,variable2,variable1 + variable2))
#realiza una resta
print("El resultado de la resta es {}-{}={}".format(variable1,variable2,variable1 - variable2))

#realiza una multiplicacion
print("El resultado de la multiplicacion es {}*{}= {}".format(variable2,variable1,variable2 * variable1))
#realiza una division
print("El resultado de la division es {}/{}= {}".format(variable1,variable2,variable1 / variable2))